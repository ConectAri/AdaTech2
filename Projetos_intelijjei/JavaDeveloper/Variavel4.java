
//Tema: Incremento de variável, diferença de ++ soma ou soma ++

public class Variavel4 {

    public static void main(String[] args) {

        int soma = 0;

        System.out.println(" A inicial  Soma o resultado é " + soma++);

        System.out.println(" A primeira Soma o resultado é " + (++soma));

        // O resultado no console fala que aqui a soma é 1 pois colocou o ++ na frente

        System.out.println(" A segunda  Soma o resultado é " + (soma++));

        System.out.println(" A terceira  Soma o resultado é " + soma);

        System.out.println(" A Quarta  Soma o resultado é " + soma++);

    }
}


// O resultado no console fala que aqui a soma é 1 pois colocou o ++ na frente
