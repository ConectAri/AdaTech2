package Aula;

import java.lang.String;

public class ElseIf {

    public static void main(String[] args) {

        System.out.println("======================");
        System.out.println(" Início da aplicação: ");
        System.out.println("======================");

        int valor = 10;

        if (valor == 20) {
            System.out.println(" Não é igual a " + valor);
        }else if(valor == 30){
            System.out.println(" Não o valor é: " + valor);
        } else if (valor ==1) {
            System.out.println(" Não o valor é: " + valor);
        }else
            System.out.println (" Sim o valor é: " + valor);

        System.out.println("Fim da aplicação!!!");

    }
}
